<h3>Rozemara Jung</h3>
<br>
<p>Rozemara Jung, 38 anos, coleciona notas 10 na graduação em Serviço Social que iniciou no ano de 2021. A dedicação às provas e a atenção em cada aula só não são maiores que o empenho que a caxiense dispensa ao tratamento que iniciou dois anos antes, contra o câncer de mama metastático.<br><br>
A situação descrita acima ocorreu quando Roze, escutou do médico que a doença a acompanharia até o fim da vida, e que o máximo a fazer seria tentar controlá-la. A partir daquele dia e daquele diagnóstico, que Roze ouviu acompanhada do filho, então com 14 anos, ela passou a receber os cuidados da equipe de cuidados paliativos do Hospital do Círculo, serviço que presta atendimento a pessoas com doenças graves e incuráveis, e que desde o início de agosto de 2022, passou a ser um setor específico no hospital, o primeiro em Caxias.<br><br>
Para Rozemara Jung, a vida ganhou novo sentido desde o diagnóstico fatídico. Após passar por um turbilhão de emoções, o apoio da família e da equipe paliativista a ajudou a ressignificar a relação com a doença e a se tornar cada vez mais disposta a dar o seu melhor, para a família, para o tratamento e para os estudos.<br><br></p>

