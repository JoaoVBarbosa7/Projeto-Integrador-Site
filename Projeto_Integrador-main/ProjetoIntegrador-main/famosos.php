<h3>Angelina Cervelin</h3>
<br>
<p>Uma semana antes de falecer, vítima das complicações de uma doença cardíaca, em outubro de 2020, Angelina Cervelin pôde comemorar os seus 80 anos com direito a bolo e decoração especial. A festa foi realizada no quarto em que a idosa estava internada no Hospital do Círculo, sob os cuidados da equipe paliativista. Com a família reunida ao redor da cama, foi feito um brinde com vinho tinto, bebida preferida da bento-gonçalvense. A própria Angelina bebeu uma taça, a qual saboreou com gosto, como recorda a afilhada Letícia Bertoldi.<br><br>
Por estar lúcida até o final da vida, Angelina pôde escolher até onde iria o seu tratamento e todos os passos foram respeitados conforme a sua vontade. Isso porque o tratamento com cuidados paliativos também se ancora nas Diretivas Antecipadas de Vontade (DAV), documento aprovado pelo Conselho Federal de Medicina em 2012, que estabelece que qualquer pessoa pode deixar registrada em cartório as suas preferências diante do fim iminente por doença grave, degenerativa e sem possibilidade de cura.<br><br></p>

