<h3>Carlos Fernandes Sobrinho</h3>
<br>
<p>Em março de 2021, o Almanaque contou a história de Carlos Fernandes Sobrinho, morador de Caxias do Sul que sofria de esclerose lateral amiotrófica, doença degenerativa que ataca o sistema nervoso e compromete funções físicas<br><br>
Carlos faleceu em outubro do mesmo ano, aos 70 anos. A filha, Jamile Fernandes, destaca como fundamental o cuidado paliativo que o pai recebeu desde o diagnóstico da doença incurável, e que acabou por vitimá-lo. Além de sessões com psicóloga, fisioterapeuta e fonoaudióloga, uma médica geriatra paliativista deu ao paciente o suporte para que seus últimos dias fossem vividos com dignidade e com a certeza de que suas vontades finais seriam respeitadas. Jamile conta que foi possível também para a família encarar a morte de Carlos com mais leveza, especialmente pelo cumprimento das diretivas antecipadas.<br><br></p>

